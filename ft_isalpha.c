#include <stdio.h>

int	ft_isalpha(int	c)
{
	if	((c <= 'Z' && c >= 'A') || (c <= 'z' && c >= 'a'))
	{
		return (1);
	}
	return (0);
}

int	main(void)
{
	int	a;

	a = ft_isalpha('p');
	printf("%d\n", a);
	return(0);
}
