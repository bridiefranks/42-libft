#include <stdio.h>

int ft_isdigit(int d)
{
	if ((d >= 0) && (d <= 9))
	{
		return (1);
	}
	return (0);
}

int main(void)
{
	int v;

	v = ft_isdigit('f');
	printf("%d\n", v);
	return (0);
}
